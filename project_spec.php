<p>
The second part of the project criteria is the back end functionality of web application. 
</p>

<p>
The web application should be implmented using Flask (any additional libraries/modules require unit coordinator approval), and provide at least the following functionality:
<ol>
<li> A user account and login feature, (as a minimum for administrators)</li>
<li> The ability to propose new question sets and add users.</li>
<li> A method to submit answers.</li>
<li> A method to assess submissions.</li>
<li> A method to see assessments.</li>
</ol>
</p>
<p>
There is a possibility for a wide variation in the degree of ambition and complexity in the project,
and this will be reflected in the marking criteria. 
For example, a quiz that assessors basic arithmetic skills using multi-choice questions and automatic grading
would probably achieve a maximum score of 75%, whilst an application that allows manual assessment and 
provides feedback to users could achieve a lot more.   
</p>
<p>
