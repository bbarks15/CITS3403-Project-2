<li> the git log, showing commits from both partners</li>
<li> all source code, with comments and attributions for any external libraries.</li>
<li> a requirements.txt file, listing all packages used. To build the requirements.txt file for your virtual environment, use the command:
<code> pip freeze > requirements.txt</code> while your virtual environment is active.
</li>
</ol>
<li>The application should include an HTML5 website with the following pages (or functions):
	
<ul>
 <li>An adminstrator view, that can add and delete question sets, assess responses, and add and delete users. 
 <li>A user view that can view question sets, and submit answers and see assessments/feedback. 
 </ul>
</li>
</ol>


