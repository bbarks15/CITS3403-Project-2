<h3>Project FAQs</h3>
<h4>Can I use jQuery, React, Bootstrap, or Angular for UIs in Project Part 1</h4>
<p>JQuery, AJAX and bootstrap are ok, but Angular, VUE.js, React etc are not.
We would like to see a demonstrated confidence in using raw HTML for structure, CSS for formatting and JavaScript for interactions in Part 1. Having said that, you are encouraged to learn these UI libraries and front-end frameworks in your spare time. 
<h4>Can I use advanced CSS3 that are not necessarily supported by certain browsers</h4>
<p><strong>Yes</strong> in this case, but you will need to clearly state the browsers that supported and ensure that your page is still functioning on the browsers installed on the CSSE/EECE lab machines. So the bottom line is, if the pages work on the browsers in the labs, then it would be considered as acceptable. 
<h4>What Flask modules can I use</h4>
Typically the flask modules mentioned in lectures will be ok. Otherwise, post a question to the help forum. The basic ones to use are:
Flask,
Flask-Babel,
Flask-Bootstrap,
Flask-HTTPAuth,
Flask-Login,
Flask-Migrate,
Flask-SQLAlchemy,
Flask-WTF,
Jinja2,
PyJWT,
requests,
Werkzeug.
 

