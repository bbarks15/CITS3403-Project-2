<h3>Web Project Specification: Online Quiz Platform</h3> 
<p><b>Due 6pm, May 25, 2020</b></p> 
<p>This project is worth <b>30%</b> of your final grade in the unit <i>must</i> be done in groups of two to four for CITS3403 students. 
For CITS5505 students, it should be done in pairs.</p> 

<p><b><a href ="./projects/Project2Criterion.pdf">Marking Criterion</a> now available</b></p>

<h2>Project Description</h2> 
<p>For this project you are required to build a multi-user quiz/questionaire/survey application. The application should be written using HTML, CSS, Flask, AJAX, JQuery, and Bootstrap. 
The application should allow administrators to set question sets, users to submit answers to the questions, and manual or automatic assessment of those answers. 
The context of the questions and the type of assessment mechanism is up to you.
</p> 

<p>Example contexts you could use are:</p> 
<ul> 
 <li>Educational assessment, like <a href=https://www.w3schools.com/js/js_exercises.asp">w3schools</a>.
 <li>Population surveys, for example to find politcal views or voting intention. (Assessment here could be showing someone where their views sit in the general population).
 <li>Research questionaires, where personal history or otehr data could be provided.
 <li>Competitive or esteem based tests, like general knowledge quizes or IQ tests
</ul> 
<p>The types of assessment mechanism you could use are</p> 
<ul> 
 <li>Multi-choice scoring 
 <li>Short text answers with fuzzy matching
 <li>Manual assessment of long text answers 
 <li>Others (logical criterion, community voting, or compiling and executing submitted code)
</ul>

 
<p>Think carefully about the design of the application.
It should be:
<ul>
<li> <i>Useful</i>, in that it answers an interesting question in a meaningful way</li>
<li> <i>Reliable</i>, so that it is not frustrating to use</li>
<li> <i>Intuitive</i>, so that it is easy for a user to complete the assessment and recieve feedback</li>
<li> <i>Engaging</i>, so that it looks good and presents sufficient context for the questions being asked.</li>

The web application should be styled to be interesting and engaging for a user in the selected context. It should offer several views including:</p> 
<ol> 
 <li>An adminstrator view, that can add delete question sets, assess responses, and add and delete users. 
 <li>A user view that can view questionsets, submit answers, and see the results of completed question sets. 
 <li>A general view that can just view aggregate results for quizzes. 
</ol> 
<p>In addition to the web application, you should create a private GitHub project that includes a readme describing</p> 
<ol> 
 <li>the purpose of the web application, explaining both the context and the assessment mechanism used. 
 <li>the architecture of the web application 
 <li>describe how to launch the web application. 
 <li>describe some unit tests for the web application, and how to run them. 
 <li>Include commit logs, showing contributions and review from both contributing students 
</ol>


